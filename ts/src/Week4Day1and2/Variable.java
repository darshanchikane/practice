package Week4Day1and2;

public class Variable {

	public static void main(String[] args) {
		
		// variable is a container to store a value
		// here x is the variable and it stores integer value as it's data type is int
		int x = 10;
		System.out.println("Number is : " + x);
		
		// here y is the variable and it stores string value as it's data type is string
		String y = "techstalwarts";
		System.out.println("Name is : " + y);
		
		

	}

}
