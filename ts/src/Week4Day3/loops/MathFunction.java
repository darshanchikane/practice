package Week4Day3.loops;

public class MathFunction {

	public static void main(String[] args) {
		
		double x = 10;
		double y = 4;
		
		System.out.println("Max : " + Math.max(x, y));
		
		System.out.println("Min : " + Math.min(x, y));
		
		System.out.println("Sqrt : " + Math.sqrt(y));

		System.out.println("Pow : " + Math.pow(x, y));
		
		System.out.println("Abs : " + Math.abs(-10));
		
		System.out.println("Round : "  + Math.round(5.5));
		
		System.out.println("Ceil : " + Math.ceil(12.1));
		
		System.out.println("Floor : " + Math.floor(12.9));
	}

}
